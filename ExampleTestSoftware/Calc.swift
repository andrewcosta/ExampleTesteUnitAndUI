import Foundation

class Calc {
    
    func sum(number1: Double, number2: Double) -> Double{
        return number1 + number2
    }
    
    func mult(number1: Double, number2: Double) -> Double{
        return number1 * number2
    }
    
    func div(number1: Double, number2: Double) -> Double{
        return number1 / number2
    }
    
    func sub(number1: Double, number2: Double) -> Double{
        return number1 - number2
    }
    
}
